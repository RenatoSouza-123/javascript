/*
>   - greater than
<   - less than
>=  - greater than or equal to
<=  - less than or equal to
==  - equality
!=  - not equal
=== - strict equality
!== - strict non-equality
*/

console.log(-2 > 1)
console.log(-2 < 1)
console.log(2 >= 2)
console.log(3 <= 2)

console.log('hello'.length > 'hello!'.length)
console.log('a' < 'A')

